package engine.ui;

/**
 * Content rendering alignment
 * <p>
 * left, the default rendering mode, the inputed X
 * coordinate being at the left-most point of the
 * element <br>
 * center is the inputed X coordinate being at the
 * center point of the element <br>
 * right is the inputed X coordinate being at the
 * right-most point of the element <br>
 * @author L2k-nForce
 */
public enum Align{
	left, right, center
}